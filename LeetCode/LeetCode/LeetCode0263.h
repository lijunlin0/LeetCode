#pragma once

/*
名称：丑数

描述：丑数 就是只包含质因数 2、3 和 5 的正整数。
     给你一个整数 n ，请你判断 n 是否为 丑数 。如果是，返回 true ；否则，返回 false 。

解题思路：将2，3，5的因数分别除尽，最后判断是否为1.
*/

#include<iostream>

class LeetCode0263
{
public:
    bool isUgly(int n) {
        if (n <= 0)
        {
            return false;
        }
        while (n % 2 == 0)
        {
            n /= 2;
        }
        while (n % 3 == 0)
        {
            n /= 3;
        }
        while (n % 5 == 0)
        {
            n /= 5;
        }
        return n == 1;
    }

    void test()
    {
        std::cout << (isUgly(6)?"true":"false");
    }
};